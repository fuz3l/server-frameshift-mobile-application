"""
Smart Flask Project Generator
Analyzes converted code and generates a proper, runnable Flask application
"""

import os
import ast
from pathlib import Path
from typing import Dict, List, Set
from ..utils.logger import logger


class SmartFlaskGenerator:
    """
    Generates a complete, runnable Flask project by analyzing the converted code
    """

    def __init__(self, project_path: str, project_name: str):
        self.project_path = Path(project_path)
        self.project_name = project_name
        self.apps = []
        self.models = []
        self.has_auth = False
        self.has_forms = False
        self.dependencies = set()

    def analyze_project(self):
        """Analyze converted code to understand project structure"""
        logger.info(f"Analyzing project: {self.project_name}")

        # Find all app directories (account, job, jobapp, etc.)
        for item in self.project_path.iterdir():
            if item.is_dir() and not item.name.startswith(('.', '__')):
                self.apps.append(item.name)

                # Check if app has models
                models_file = item / 'models.py'
                if models_file.exists():
                    self._analyze_models(models_file, item.name)

                # Check if app has routes
                routes_file = item / 'routes.py'
                if routes_file.exists():
                    self._analyze_routes(routes_file)

        logger.info(f"Found {len(self.apps)} apps: {', '.join(self.apps)}")
        logger.info(f"Found {len(self.models)} models")

    def _analyze_models(self, models_file: Path, app_name: str):
        """Analyze models file to find model classes"""
        try:
            content = models_file.read_text(encoding='utf-8')

            # Simple regex to find class definitions
            import re
            classes = re.findall(r'class\s+(\w+)\s*\(.*\):', content)

            for class_name in classes:
                self.models.append({
                    'name': class_name,
                    'app': app_name
                })

            # Check for authentication models
            if 'User' in classes or 'CustomUser' in classes:
                self.has_auth = True

        except Exception as e:
            logger.warn(f"Error analyzing {models_file}: {e}")

    def _analyze_routes(self, routes_file: Path):
        """Analyze routes to understand features"""
        try:
            content = routes_file.read_text(encoding='utf-8')

            # Check for forms
            if 'WTForms' in content or 'FlaskForm' in content:
                self.has_forms = True

        except Exception as e:
            logger.warn(f"Error analyzing {routes_file}: {e}")

    def generate_app_py(self) -> str:
        """Generate main app.py file"""

        blueprints_import = []
        blueprints_register = []

        for app in self.apps:
            bp_name = f"{app}_bp"
            blueprints_import.append(f"from {app}.routes import {bp_name}")
            blueprints_register.append(f"    app.register_blueprint({bp_name}, url_prefix='/{app}')")

        blueprints_import_str = '\n'.join(blueprints_import) if blueprints_import else "    # No blueprints found"
        blueprints_register_str = '\n'.join(blueprints_register) if blueprints_register else "    # No blueprints to register"

        auth_imports = ""
        auth_init = ""
        if self.has_auth:
            auth_imports = "\nfrom flask_login import LoginManager"
            auth_init = """
    # Initialize Flask-Login
    login_manager = LoginManager()
    login_manager.init_app(app)
    login_manager.login_view = 'account.login'

    @login_manager.user_loader
    def load_user(user_id):
        from account.models import User
        return User.query.get(int(user_id))
"""

        return f'''"""
{self.project_name} - Flask Application
Auto-generated by FrameShift
"""

from flask import Flask
from flask_sqlalchemy import SQLAlchemy
from flask_migrate import Migrate{auth_imports}

# Initialize extensions
db = SQLAlchemy()
migrate = Migrate()

def create_app(config_name='development'):
    """Application factory pattern"""
    app = Flask(__name__)

    # Load configuration
    from config import config
    app.config.from_object(config[config_name])

    # Initialize extensions
    db.init_app(app)
    migrate.init_app(app, db)
{auth_init}

    # Register blueprints
{blueprints_register_str}

    # Import models to ensure they're registered with SQLAlchemy
{self._generate_model_imports()}

    # Create database tables
    with app.app_context():
        db.create_all()

    @app.route('/')
    def index():
        return "Welcome to {self.project_name}!"

    return app

if __name__ == '__main__':
    app = create_app()
    app.run(debug=True, host='0.0.0.0', port=5000)
'''

    def _generate_model_imports(self) -> str:
        """Generate imports for all models"""
        imports = []
        apps_done = set()

        for model in self.models:
            if model['app'] not in apps_done:
                imports.append(f"    from {model['app']}.models import *")
                apps_done.add(model['app'])

        return '\n'.join(imports) if imports else "    # No models found"

