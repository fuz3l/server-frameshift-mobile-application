    def _generate_structure_tree(self) -> str:
        """Generate ASCII tree of app structure"""
        tree_lines = []
        for app in self.apps:
            tree_lines.append(f"├── {app}/")
            tree_lines.append(f"│   ├── models.py")
            tree_lines.append(f"│   ├── routes.py")
            tree_lines.append(f"│   └── views.py")
        return '\n'.join(tree_lines) if tree_lines else ""

    def generate_all(self):
        """Generate all Flask project files"""
        logger.info(f"Generating runnable Flask project: {self.project_name}")

        # Analyze the project first
        self.analyze_project()

        # Generate files
        files = {
            'app.py': self.generate_app_py(),
            'config.py': self.generate_config_py(),
            'requirements.txt': self.generate_requirements_txt(),
            '.env.example': self.generate_env_example(),
            '.gitignore': self.generate_gitignore(),
            'README.md': self.generate_readme_md(),
        }

        # Write files
        for filename, content in files.items():
            file_path = self.project_path / filename
            try:
                file_path.write_text(content, encoding='utf-8')
                logger.info(f"Generated: {filename}")
            except Exception as e:
                logger.error(f"Failed to generate {filename}: {e}")

        logger.info(f"Flask project generation complete!")
        return {
            'success': True,
            'files_generated': list(files.keys()),
            'apps': self.apps,
            'models': len(self.models)
        }
